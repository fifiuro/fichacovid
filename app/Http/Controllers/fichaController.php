     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ficha = new Ficha;

        $ficha->establecimiento = $request->establecimiento;
        $ficha->codigo_estado = $request->codigo;
        $ficha->red_salud = $request->red;
        $ficha->departamento = $request->departamento;
        $ficha->municipio = $request->municipio;
        $ficha->fecha_notificacion = $request->fecha_notificacion;
        $ficha->sem_epidem = $request->sem_epidem;
        $ficha->caso_identificado = $request->caso_identificado;


        $ficha->save();
    }

